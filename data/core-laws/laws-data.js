// 导出数据和工具函数
module.exports = {
  lawsData,
  // 获取所有法条
  getAllLaws: () => lawsData,
  // 按阶段获取法条
  getLawsByStage: (stage) => lawsData.filter(law => law.stage === stage),
  // 按标签获取法条
  getLawsByTag: (tag) => lawsData.filter(law => law.tag === tag),
  // 根据ID获取法条
  getLawById: (id) => lawsData.find(law => law.id === id),
  // 搜索法条
  searchLaws: (keyword) => {
    if (!keyword || keyword.trim() === '') return [];
    const searchTerm = keyword.toLowerCase().trim();
    return lawsData.filter(law => 
      law.title.toLowerCase().includes(searchTerm) ||
      law.summary.toLowerCase().includes(searchTerm) ||
      law.tag.toLowerCase().includes(searchTerm) ||
      law.legalBasis.toLowerCase().includes(searchTerm)
    );
  },
  // 获取统计信息
  getStats: () => {
    const stages = {};
    const tags = {};
    lawsData.forEach(law => {
      stages[law.stage] = (stages[law.stage] || 0) + 1;
      tags[law.tag] = (tags[law.tag] || 0) + 1;
    });
    return {
      total: lawsData.length,
      stages,
      tags
    };
  }
};
